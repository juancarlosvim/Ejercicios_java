            // Boton grabar:

            PreparedStatement pst = cn.prepareStatement("INSERT INTO productos (codpro, despro, prepro) VALUES (?,?,?)");
            
            pst.setString(1, txtCodigo.getText());
            pst.setString(2, txtDescripcion.getText());
