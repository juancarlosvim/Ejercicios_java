    private void btnEdadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdadActionPerformed
        // Boton enviar:
        
        String[] datos = new String[3];
        
        datos[0] = txtNombre.getText();
        txtNombre.setText(null);
         datos[1] = txtApellidos.getText();
        txtApellidos.setText(null);
         datos[2] = txtEdad.getText();
        txtEdad.setText(null);
        
        modelo.addRow(datos);
    }//GEN-LAST:event_btnEdadActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Boton eliminar:
        
        int filaSeleccionada = jTable1.getSelectedRow();
        
        if(filaSeleccionada>=0){
            modelo.removeRow(filaSeleccionada);
        }else{
            JOptionPane.showMessageDialog(null, "Tabla vacia o no selecciono algún registro");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEliminarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTodoActionPerformed
        // Eliminar todo
        
        int cantidadFilas = jTable1.getRowCount();
        
        for(int i=cantidadFilas-1;i>=0;i--){
            modelo.removeRow(i);
        }
    }//GEN-LAST:event_btnEliminarTodoActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // boton modificar
        
        int fila = Integer.parseInt(txtFila.getText());
        int columna = Integer.parseInt(txtComulna.getText());
        
        modelo.setValueAt(txtNuevoValor.getText(), fila, columna);
    }//GEN-LAST:event_btnModificarActionPerformed

