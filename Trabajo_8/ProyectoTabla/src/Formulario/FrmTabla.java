    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // dentro de la tabla mouse clicked:
        
        int filasele = jTable1.getSelectedRow();
        txtNombre.setText(jTable1.getValueAt(filasele, 0).toString());
        txtApellidos.setText(jTable1.getValueAt(filasele, 1).toString());
        txtEdad.setText(jTable1.getValueAt(filasele, 2).toString());
        
        
        filas = filasele;
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO
        
        /* el boton actualizar lo que hace que el contenido que hay que me hemos selecionado
        de la tabla  se pone en los campos Nombre, Apellidos, Edad  y si cambiamos esos campos
        por un nombre nuevo  y le damos a actualizar se cambiara el registro entero
        
        
        */
        String[] datos = new String[3];
         datos[0] = txtNombre.getText();
        
         datos[1] = txtApellidos.getText();
       
         datos[2] = txtEdad.getText();
         
         for(int i=0; i<jTable1.getColumnCount();i++){
             modelo.setValueAt(datos[i], filas, i);
         }
    
        
    }//GEN-LAST:event_btnActualizarActionPerformed

